func drawMap(s tcell.Screen, inMap [][]int, style tcell.Style) {
	// TODO: is this legit? what if becomes float?
	numRows := len(inMap)
	numCols := len(inMap[0])
	for x := 0; x < numCols; x++ {
		for y := 0; y < numRows; y++ {
			if worldMap[y][x] != 0 {
				setContentEqualWidth(s, x, y, ' ', nil, styleMap[worldMap[y][x]])
			}
		}
	}
}
